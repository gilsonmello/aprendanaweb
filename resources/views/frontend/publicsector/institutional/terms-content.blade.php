<p>a) Os nossos Produtos são oferecidos exclusivamente na forma on-line. Nossas aulas e

    vídeo comentários são gravados, editados e posteriormente disponibilizados para

    visualização, após a confirmação do pagamento do valor do respectivo Curso, o que se

    dará em um prazo de até 02 dias úteis.</p>

<p>    b) Para assistir as vídeoaulas e/ou vídeos comentários é necessário utilizar serviço de

    internet banda larga, de no mínimo 2 MB. Sugerimos a não utilização de serviço de internet

    do tipo 3G e 4G. Recomendamos que antes da aquisição de qualquer Produto, seja

    verificado pelo usuário a capacidade de sua conexão mediante a realização de testes com

    os vídeos gratuitos disponibilizados no site, evitando assim, problemas futuros.</p>

<p> c) Após a efetivação da Inscrição, o (a) aluno (a) terá o tempo de acesso do respectivo

    Curso de até 90 (noventa) dias para visualizar as vídeoaulas e os vídeos comentários,

    conforme constar na descrição do Produto que tenha adquirido. Após o decurso do prazo,

    que é IMPRORROGÁVEL, os acessos serão finalizados.</p>

<p> d) Poderá haver disponibilização de material didático, a critério do professor, da respectiva

    disciplina, e o acesso se dará via site, para os alunos regularmente inscritos.</p>

<p> e) Após o fim do prazo de duração do Curso, o (a) aluno (a) não terá mais acesso ao

    material didático, sendo possível baixá-lo até a data prevista da expiração do curso.</p>

<p> f) As vídeoaulas e/ou vídeo comentários somente poderão ser acessadas mediante login e

    senha no site www.brasiljuridico.com.br/gestaopublica e estarão disponíveis apenas para

    os alunos regularmente inscritos, sendo vedada a distribuição a terceiros.</p>

<p>    g) As vídeoaulas dos cursos são divididas em blocos de aproximadamente 20 minutos cada.</p>

<p> h) Cada bloco do curso poderá ser visualizado pelo aluno até 02 (duas) vezes, de acordo

    com Curso adquirido e nos horários que lhe for conveniente. Cursos específicos poderão ter

    o número de acessos maior, a critério da coordenação do curso, o que será informado na

    ficha técnica do curso.</p>

<p> i) Será computado como visualizado cada bloco de aula do curso após a visualização de

    70% (setenta por cento) do tempo de cada bloco.</p>

<p> j) É vedado que as aulas dos Cursos e os vídeo comentários da Avaliação Exclusiva de

    Performance sejam, total ou parcialmente, baixadas para arquivo pessoal do aluno.</p>

<p> k) São vedados acessos simultâneos. Verificada a ocorrência pelo nosso sistema, a conexão

    será automaticamente finalizada.</p>

<p> l) É imprescindível a realização do cadastro do (a) aluno (a) para acesso às vídeoaulas e

    aos vídeo comentários. Os dados pessoais informados pelos usuários não serão divulgados

    para terceiros.</p>

<p> m) Após a efetiva disponibilização de todas as aulas e todos os vídeo comentários, fica

    vedado o cancelamento do Curso, independentemente de terem sido ou não assistidas,

    total ou parcialmente.</p>

<p>    n) Em caso de desistência do Produto, antes da disponibilização de todas as aulas, terá o

    (a) aluno (a) direito a restituição do valor pago. Neste caso, será descontado o valor

    proporcional às aulas já disponibilizadas até a data do cancelamento, independentemente

    de terem sido ou não assistidas, total ou parcialmente, com a incidência de multa rescisória

    de 20% (vinte por cento) sobre o valor total do curso pago. Poderá o (a) aluno (a),

    mediante solicitação, optar pelo recebimento do valor a ser restituído em forma de bônus

    em outros cursos que estejam disponíveis no momento da desistência.</p>

<p> o) É vedada a troca entre Cursos, em virtude da diversidade do número de aulas,

    disciplinas lecionadas, prazo de duração dos cursos, custos da instituição, contratação de

    professores, dentre outras.</p>

<p> p) Em razão das disponibilidades dos professores integrantes do corpo docente dos

    respectivos Cursos, poderão ocorrer substituições, a critério da Coordenação Pedagógica do

    Curso.</p>

<p>q) Os Certificados serão emitidos por meio eletrônico mediante a solicitação do (a) aluno

    (a), através do campo &quot;fale conosco&quot; na página inicial do site. O aluno(a) tem o prazo de

    até doze meses, após expirado o prazo de duração do curso adquirido para fazer a referida

    solicitação. Em caso de solicitação do Certificado antes da expiração do tempo de acesso

    do Curso, o Certificado será emitido e serão finalizados os acessos.</p>

